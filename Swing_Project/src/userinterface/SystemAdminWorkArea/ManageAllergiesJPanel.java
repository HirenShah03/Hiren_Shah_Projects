    private void txtThresholdKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtThresholdKeyPressed
        // TODO add your handling code here:
                       int key = evt.getKeyCode();
        if ((key >= evt.VK_0 && key <= evt.VK_9) || (key >= evt.VK_NUMPAD0 && key <= evt.VK_NUMPAD9)
                || (key == evt.VK_BACK_SPACE || key == evt.VK_DELETE)) {
            txtThreshold.setEditable(true);
            txtThreshold.setBackground(Color.WHITE);
        } else {

            txtThreshold.setEditable(false);
            txtThreshold.setBackground(Color.red);

        }

    }//GEN-LAST:event_txtThresholdKeyPressed


