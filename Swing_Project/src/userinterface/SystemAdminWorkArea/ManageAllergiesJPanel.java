    public void populateAllergy(){
        DefaultTableModel model = (DefaultTableModel) tblAllergies.getModel();
        model.setRowCount(0);
        for(Allergy a : system.getAllergyList()){
            Object [] row = new Object[model.getColumnCount()];
            row[0] = a;
            row[1] = a.getSymptoms();
            row[2] = a.getPrecautions();
            row[3] = a.getRemedy();
            model.addRow(row);
        }
    }

