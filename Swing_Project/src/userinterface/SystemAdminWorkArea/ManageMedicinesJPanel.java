    public void populateMedicines(){
        DefaultTableModel model = (DefaultTableModel) tblMedicines.getModel();
        model.setRowCount(0);
        for (Medicine medicine : system.getMedicineList()) {
            Object[] row = new Object[model.getColumnCount()];
            row[0] = medicine;
            row[1] = medicine.getPrice();
            model.addRow(row);
        }
    }

