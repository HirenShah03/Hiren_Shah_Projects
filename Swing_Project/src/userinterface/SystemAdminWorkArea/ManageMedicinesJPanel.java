    private void btnBack2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBack2ActionPerformed
        // TODO add your handling code here:
                if(!(txtName.getText().isEmpty() || txtPrice.getText().isEmpty())){
            if(!Pattern.matches("[0-9.]*", txtPrice.getText())){
                JOptionPane.showMessageDialog(null, "Please enter Price in digits between 0-9 only!!","Warning",JOptionPane.WARNING_MESSAGE);
            }else{
                if(this.medicine != null){
                    this.medicine.setName(txtName.getText());
                    this.medicine.setPrice(Double.parseDouble(txtPrice.getText()));
                }else{
                    Medicine med = new Medicine();
                    med.setName(txtName.getText());
                    med.setPrice(Double.parseDouble(txtPrice.getText()));
                    system.getMedicineList().add(med);
                }
                JOptionPane.showMessageDialog(null, "Medicine has been added successfully!");
                this.medicine = null;
                populateMedicines();
            }
        }else{
            JOptionPane.showMessageDialog(null, "Name and Price of the medicine cannot be blank!","Warning",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnBack2ActionPerformed

    private void btnBack3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBack3ActionPerformed
        // TODO add your handling code here:
                int selectedRow = tblMedicines.getSelectedRow();
        if(selectedRow < 0)
        {
            JOptionPane.showMessageDialog(null,"Please select row","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            this.medicine = (Medicine) tblMedicines.getValueAt(selectedRow,0);
            txtName.setText(this.medicine.getName());
            txtPrice.setText(String.valueOf(this.medicine.getPrice()));
        }
    }//GEN-LAST:event_btnBack3ActionPerformed

    private void btnBack4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBack4ActionPerformed
        // TODO add your handling code here:
                int selectedRow = tblMedicines.getSelectedRow();
        if(selectedRow < 0)
        {
            JOptionPane.showMessageDialog(null,"Please select row","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            this.medicine = (Medicine) tblMedicines.getValueAt(selectedRow,0);
            system.getMedicineList().remove(this.medicine);
            JOptionPane.showMessageDialog(null,"Medicine Details deleted successfully!");
            this.medicine = null;
        }
        populateMedicines();
    }//GEN-LAST:event_btnBack4ActionPerformed

