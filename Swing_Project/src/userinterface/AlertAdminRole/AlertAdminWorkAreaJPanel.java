    public void populateEpidemics(){
        DefaultTableModel model = (DefaultTableModel)tblEpidemics.getModel();
        model.setRowCount(0);
        for(Epidemic epidemic : ((AlertOrganization)organization).getEpidemicList()){
            Object [] row = new Object[model.getColumnCount()];
            row[0] = epidemic;
            row[1] = epidemic.getPrecautions();
            row[2] = epidemic.getVaccinations();
            model.addRow(row);
        }
    }
