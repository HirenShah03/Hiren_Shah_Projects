    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed

        // TODO add your handling code here:
        int selectedRow = tblEpidemics.getSelectedRow();
        if(selectedRow < 0)
        {
            JOptionPane.showMessageDialog(null,"Please select row","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            this.epidemic = (Epidemic) tblEpidemics.getValueAt(selectedRow,0);
            txtEpidemicOf.setText(this.epidemic.getEpidemicOf());
            txtPrecautions.setText(this.epidemic.getPrecautions());
            txtVaccinations.setText(this.epidemic.getVaccinations());
        }
        
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
         // TODO add your handling code here:
        int selectedRow = tblEpidemics.getSelectedRow();
        if(selectedRow < 0)
        {
            JOptionPane.showMessageDialog(null,"Please select row","Warning",JOptionPane.WARNING_MESSAGE);
        }
        else
        {
            this.epidemic = (Epidemic) tblEpidemics.getValueAt(selectedRow,0);
            ((AlertOrganization)organization).getEpidemicList().remove(this.epidemic);
            JOptionPane.showMessageDialog(null,"Epidemic Details deleted successfully!");
            populateEpidemics();
            this.epidemic = null;
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        // TODO add your handling code here:
        if(!(txtEpidemicOf.getText().isEmpty() || txtPrecautions.getText().isEmpty() || txtVaccinations.getText().isEmpty())){
            if(this.epidemic == null){
                this.epidemic = new Epidemic();
                ((AlertOrganization)organization).getEpidemicList().add(epidemic);
            }
            this.epidemic.setEpidemicOf(txtEpidemicOf.getText());
            this.epidemic.setPrecautions(txtPrecautions.getText());
            this.epidemic.setVaccinations(txtVaccinations.getText());
            JOptionPane.showMessageDialog(null, "Epidemic has been added successfully!");
            this.epidemic = null;
            populateEpidemics();
        }else{
            JOptionPane.showMessageDialog(null, "Please fill in all the details regarding the Epidemic!","Warning",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed


