    private void btnAssignToMeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignToMeActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblGlobalWR.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a Work Request to assign it to yourself!");
        }else{
            WorkRequest wr = (WorkRequest) tblGlobalWR.getValueAt(selectedRow, 0);
            ((LabTestWorkRequest)wr).setLabAssigned(account);
            account.getWorkQueue().getWorkRequestList().add(wr);
            wr.setStatus("Assigned");
            populateGlobalWorkRequests();
            populateSelfWorkRequests();
        }
    }//GEN-LAST:event_btnAssignToMeActionPerformed

