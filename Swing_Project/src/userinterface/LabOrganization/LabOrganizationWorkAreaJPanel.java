    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        if(txtMessage.getText().isEmpty() || txtTestAttribute.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter all the details of the test result!","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        int selectedRow = tblGlobalWR.getSelectedRow();
        if(selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a Work Request!");
        }else{
            WorkRequest wr = (WorkRequest) tblGlobalWR.getValueAt(selectedRow, 0);
            Result r = new Result();
            r.setAttribute(txtTestAttribute.getText());
            r.setResult(txtMessage.getText());
            ((LabTestWorkRequest)wr).getTestResults().add(r);
            wr.setStatus("Pending");
            lblAttribute.setEnabled(true);
            lblMessage.setEnabled(true);
            txtTestAttribute.setEnabled(true);
            txtMessage.setEnabled(true);
            JOptionPane.showMessageDialog(null, "Test Result added successfully!");
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

