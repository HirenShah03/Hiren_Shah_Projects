    public void populateGlobalWorkRequests(){
        DefaultTableModel model = (DefaultTableModel)tblGlobalWR.getModel();
        model.setRowCount(0);
        
        for(Organization org : enterprise.getOrganizationDirectory().getOrganizationList()){
            if(org instanceof LabOrganization){
                for(WorkRequest wr : org.getWorkQueue().getWorkRequestList()){
                    if(((LabTestWorkRequest)wr).getLabAssigned()==null){
                        Object [] row = new Object[model.getColumnCount()];
                        row[0] = wr;
                        row[1] = wr.getTitle();
                        model.addRow(row);
                    }
                }
            }
        }
    }
    
    public void populateSelfWorkRequests(){
        DefaultTableModel model = (DefaultTableModel)tblWR.getModel();
        model.setRowCount(0);
        
        for(WorkRequest wr : account.getWorkQueue().getWorkRequestList()){
            if(wr.getStatus()=="Completed"){
                Object [] row = new Object[model.getColumnCount()];
                row[0] = wr;
                row[1] = wr.getTitle();
                model.addRow(row);
            }
        }
        
    }

