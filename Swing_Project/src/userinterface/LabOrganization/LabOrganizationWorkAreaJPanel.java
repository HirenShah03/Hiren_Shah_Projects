    public void populateTestResults(){
        int selectedRow = tblWR.getSelectedRow();
        LabTestWorkRequest lwr = (LabTestWorkRequest)tblWR.getValueAt(selectedRow, 0);
        DefaultTableModel model = (DefaultTableModel)tblWR.getModel();
        model.setRowCount(0);
        
        for(Result result : lwr.getTestResults()){
            Object [] row = new Object[model.getColumnCount()];
            row[0] = result.getAttribute();
            row[1] = result.getResult();
            model.addRow(row);
        }
    }

