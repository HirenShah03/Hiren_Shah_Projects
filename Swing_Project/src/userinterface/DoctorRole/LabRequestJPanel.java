    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel)tblIRQueue.getModel();
        dtm.setRowCount(0);
        for(WorkRequest wr : ua.getWorkQueue().getWorkRequestList()){
            if(wr instanceof LabTestWorkRequest){
                Object [] row = new Object[dtm.getColumnCount()];
                row[0] = wr;
                row[1] = wr.getTitle();
                dtm.addRow(row);
            }
        }
    }
    
    public void populateResults(LabTestWorkRequest wr){
        DefaultTableModel dtm = (DefaultTableModel)tblResults.getModel();
        dtm.setRowCount(0);
        
        for(Result r : wr.getTestResults()){
            Object [] row = new Object[dtm.getColumnCount()];
            row[0] = r.getAttribute();
            row[1] = r.getResult();
            dtm.addRow(row);
        }
    }

