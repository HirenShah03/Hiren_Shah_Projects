    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if(txtTitle1.getText().isEmpty() || txtDescription1.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Please enter the Title and the Description of the Consultation!","Warning",JOptionPane.WARNING_MESSAGE);
            return;
        }
        LabTestWorkRequest lwr = new LabTestWorkRequest();
        wr.setTitle(txtTitle1.getText());
        Conversation c = new Conversation();
        c.setUa(ua);
        c.setMsg(txtDescription1.getText());
        wr.getConversation().add(c);
        ua.getWorkQueue().getWorkRequestList().add(lwr);
        wr.getLabTests().add(lwr);
        for(Organization org : enterprise.getOrganizationDirectory().getOrganizationList()){
            if(org instanceof LabOrganization){
                org.getWorkQueue().getWorkRequestList().add(lwr);
            }
        }
        JOptionPane.showMessageDialog(null, "Lab Request has been Raised Successfully!");
        populateTable();
    }//GEN-LAST:event_jButton2ActionPerformed


